// 외워서 하는 존재 - x
// 자꾸 반복을 하니 외움을 당하는 존재


public class VariableMain {
	public static void main(String[] args) {
		// 식당 이름이 김밥천국
		String rName = "김밥지옥";
		
		// 테이블 수가 10
		int tableCount = 10;
		
		// 별점 4.5
		double starScore = 4.5;
		
		// 영업중
		boolean open = true;
		
		System.out.println("restaurantName");
		System.out.println(rName);
		System.out.printf("식당명\t: %s\n", rName);
		System.out.printf("테이블\t: %d자리\n", tableCount);
		System.out.printf("별점\t: %.1f점\n", starScore);
		System.out.printf("영업중\t: %b", open);
	}
}





